package com.nikolamarincic.fpscaler;


public class FrameCoords {
	public int posX = 0;
	public int posY = 0;
	public int sizeX = 100;
	public int sizeY = 100;

	public FrameCoords(int posx, int posy, int sizex, int sizey) {
		this.posX = posx;
		this.posY = posy;
		this.sizeX = sizex;
		this.sizeY = sizey;
	}
}
